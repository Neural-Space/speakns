const getSuburl = (language, domain) => {
    let suburl = ""
    sttLanguageDomainModelMap[language].forEach(model => {
        if (model.domain === domain) {
            suburl = model.suburl
        }
    })
    return suburl
}

const getSupportedSampleRate = (language, suburl) => {
    let sampleRate = 16000
    sttLanguageDomainModelMap[language].forEach(d => {
        if (d.suburl === suburl) {
            sampleRate = d.sampleRate
        }
    });
    return sampleRate
}
